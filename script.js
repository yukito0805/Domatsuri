// スケジュール更新（teamListまたはfavoriteTeamListからの選択に対応）
function updateSchedules(selectedTeams) {
    scheduleList.innerHTML = "";
    combinedScheduleList.innerHTML = "";
    
    // 選択がない場合
    if (selectedTeams.length === 0) {
        const scheduleOption = document.createElement("option");
        scheduleOption.text = "チームを選択してスケジュールを表示";
        scheduleList.appendChild(scheduleOption);
        
        const combinedOption = document.createElement("option");
        combinedOption.text = "複数チームを選択して合算スケジュールを表示";
        combinedScheduleList.appendChild(combinedOption);
        return;
    }
    
    // イベント名を取得
    const eventName = eventInput.value.trim();
    
    // 単一チームのスケジュール（最初の選択チーム）を表示
    const firstSelectedTeam = selectedTeams[0];
    const team = eventData[eventName]?.find(t => t.name === firstSelectedTeam);
    const schedules = team?.schedules || [];
    
    if (schedules.length === 0) {
        const scheduleOption = document.createElement("option");
        scheduleOption.text = "スケジュールが見つかりません";
        scheduleList.appendChild(scheduleOption);
    } else {
        schedules.forEach(schedule => {
            const option = document.createElement("option");
            option.text = `${schedule.date} ${schedule.venue} ${schedule.time}`;
            scheduleList.appendChild(option);
        });
    }
    
    // 選択された複数チームのスケジュールを合算してソート
    const combinedSchedules = [];
    selectedTeams.forEach(teamName => {
        const team = eventData[eventName]?.find(t => t.name === teamName);
        if (team && team.schedules) {
            team.schedules.forEach(schedule => {
                combinedSchedules.push({ teamName, schedule });
            });
        }
    });
    
    // 日付・時間順にソート
    combinedSchedules.sort((a, b) => {
        const dateTimeA = parseDateTime(a.schedule.date, a.schedule.time);
        const dateTimeB = parseDateTime(b.schedule.date, b.schedule.time);
        return dateTimeA - dateTimeB;
    });
    
    // 合算スケジュールをリストボックスに追加（日付ごとの色分け）
    if (combinedSchedules.length === 0) {
        const combinedOption = document.createElement("option");
        combinedOption.text = "スケジュールが見つかりません";
        combinedScheduleList.appendChild(combinedOption);
    } else {
        combinedSchedules.forEach(({ teamName, schedule }) => {
            const option = document.createElement("option");
            option.text = `${schedule.date} ${schedule.time} ${teamName} ${schedule.venue}`;
            if (schedule.date.includes("8月29日")) {
                option.classList.add("friday");
            } else if (schedule.date.includes("8月30日")) {
                option.classList.add("saturday");
            } else if (schedule.date.includes("8月31日")) {
                option.classList.add("sunday");
            }
            combinedScheduleList.appendChild(option);
        });
    }
}

