// 日付と時間をパースして比較可能な形式に変換
function parseDateTime(dateStr, timeStr) {
    const dateMatch = dateStr.match(/(\d{4})年(\d{1,2})月(\d{1,2})日/);
    const timeMatch = timeStr.match(/(\d{1,2}):(\d{2})/);
    
    if (!dateMatch || !timeMatch) return new Date(0);
    
    const [, year, month, day] = dateMatch;
    const [, hour, minute] = timeMatch;
    
    return new Date(year, month - 1, day, hour, minute);
}

// チームリストを更新（お気に入りを上に表示）
function updateTeamList(teams) {
    teamList.innerHTML = "";
    if (teams.length === 0) {
        const div = document.createElement("div");
        div.textContent = "該当するイベントなし";
        teamList.appendChild(div);
        return;
    }
    
    // お気に入りと非お気に入りを分ける
    const favoriteTeams = teams.filter(team => favorites.includes(team.name));
    const nonFavoriteTeams = teams.filter(team => !favorites.includes(team.name));
    
    // お気に入りチームを上に（星マーク付き）
    favoriteTeams.forEach(team => {
        const label = document.createElement("label");
        const checkbox = document.createElement("input");
        checkbox.type = "checkbox";
        checkbox.value = team.name;
        checkbox.classList.add("team-checkbox");
        label.textContent = `⭐ ${team.name}`;
        label.prepend(checkbox);
        teamList.appendChild(label);
    });
    
    // 非お気に入りチーム
    nonFavoriteTeams.forEach(team => {
        const label = document.createElement("label");
        const checkbox = document.createElement("input");
        checkbox.type = "checkbox";
        checkbox.value = team.name;
        checkbox.classList.add("team-checkbox");
        label.textContent = team.name;
        label.prepend(checkbox);
        teamList.appendChild(label);
    });
}

